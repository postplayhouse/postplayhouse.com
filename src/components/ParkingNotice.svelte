<h2 class="h2">Parking</h2>

<p>
  Please note that Post Playhouse's venue is located on Ft. Robinson State Park.
  As with all Nebraska State Parks, a parking permit is required for entry.
  Daily and annual parking passes may be purchased at the theatre within 30
  minutes of show times, or at the Lodge nearby. The State officers usually
  patrol the park during show times, so please do not forget to purchase a pass.
  Parking passes can also be purchased at the
  <a href="https://ngpc-permits.ne.gov/NGPC-PS/faces/public/welcome"
    >Nebraska Game and Parks Commission website</a
  >.
</p>
